package com.melon.music.vo;

public class MusicVO {

	private String mscId;
	private String albmId;
	private String ttl;
	private int lkCnt;
	private String mp3Fl;
	private String mscn;
	private String drtr;
	private String lrcs;
	public String getMscId() {
		return mscId;
	}
	public void setMscId(String mscId) {
		this.mscId = mscId;
	}
	public String getAlbmId() {
		return albmId;
	}
	public void setAlbmId(String albmId) {
		this.albmId = albmId;
	}
	public String getTtl() {
		return ttl;
	}
	public void setTtl(String ttl) {
		this.ttl = ttl;
	}
	public int getLkCnt() {
		return lkCnt;
	}
	public void setLkCnt(int lkCnt) {
		this.lkCnt = lkCnt;
	}
	public String getMp3Fl() {
		return mp3Fl;
	}
	public void setMp3Fl(String mp3Fl) {
		this.mp3Fl = mp3Fl;
	}
	public String getMscn() {
		return mscn;
	}
	public void setMscn(String mscn) {
		this.mscn = mscn;
	}
	public String getDrtr() {
		return drtr;
	}
	public void setDrtr(String drtr) {
		this.drtr = drtr;
	}
	public String getLrcs() {
		return lrcs;
	}
	public void setLrcs(String lrcs) {
		this.lrcs = lrcs;
	}
	
}
